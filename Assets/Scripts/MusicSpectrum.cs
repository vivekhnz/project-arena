    internal float GetIntensity()
    {
#if UNITY_WEBGL
        // WebGL doesn't support performing an FFT at runtime, so we'll cheat by pulsing in time
        // with the beat and modulating by a hardcoded table of amplitude values.
        float bpm = 140;
        float secondsPerBeat = 60.0f / bpm;
        float secondsUntilFirstBeat = 0.3f;
        float elapsedTime = audioSource.time + secondsUntilFirstBeat;
        float beatTime = (elapsedTime % secondsPerBeat) / secondsPerBeat;
        float beatIntensity = Mathf.Pow(beatTime, 3);

        int amplitudeLookupPos = (int)((audioSource.time * 1000.0f) / 100.0f);
        float amplitude = 0;
        if (amplitudeLookupPos < musicAmplitudeValues.Length)
        {
            float ampValue = musicAmplitudeValues[amplitudeLookupPos];
            amplitude = Mathf.Pow(ampValue * 0.5f, 3);
        }

        return beatIntensity * amplitude;
#else
        audioSource.GetSpectrumData(spectrum, 0, FFTWindow.BlackmanHarris);
        return spectrum[2];
#endif
    }

